-- Unified generation view: merges all energy sources in one place
CREATE VIEW v_generation_by_source AS
SELECT
    gr.tenant_id,
    t.name                      AS tenant_name,
    gr.plant_id,
    p.plant_name,
    p.state,
    est.code                    AS source_type,
    d.serial_number,
    gr.reading_date,
    gr.reading_time,
    gr.generation_value,
    gr.generation_before_losses,
    (gr.generation_before_losses - gr.generation_value) AS transmission_loss
FROM generation_readings       gr
JOIN tenants                   t   ON t.tenant_id    = gr.tenant_id
JOIN plants                    p   ON p.plant_id     = gr.plant_id
JOIN devices                   d   ON d.device_id    = gr.device_id
JOIN energy_source_types       est ON est.source_type_id = gr.source_type_id;

-- Plant capability view: shows what each plant can generate
CREATE VIEW v_plant_capabilities AS
SELECT
    p.plant_id,
    p.tenant_id,
    t.name                              AS tenant_name,
    t.tenant_code,
    p.plant_name,
    p.location,
    p.state,
    p.total_capacity_mw,
    STRING_AGG(est.code, ' + ' ORDER BY est.code) AS energy_sources,
    BOOL_OR(est.code = 'SOLAR')         AS has_solar,
    BOOL_OR(est.code = 'WIND')          AS has_wind,
    COUNT(DISTINCT pes.source_type_id)  AS source_count,
    SUM(pes.installed_capacity_mw)      AS total_installed_mw
FROM plants                p
JOIN tenants               t   ON t.tenant_id    = p.tenant_id
JOIN plant_energy_sources  pes ON pes.plant_id   = p.plant_id
JOIN energy_source_types   est ON est.source_type_id = pes.source_type_id
WHERE pes.is_active = TRUE
GROUP BY p.plant_id, p.tenant_id, t.name, t.tenant_code, p.plant_name,
         p.location, p.state, p.total_capacity_mw;

-- Monthly generation summary per plant per source
CREATE VIEW v_monthly_generation AS
SELECT
    tenant_id,
    plant_id,
    source_type_id,
    DATE_TRUNC('month', reading_date)   AS month,
    SUM(generation_value)               AS total_generation_kwh,
    SUM(generation_before_losses)       AS gross_generation_kwh,
    SUM(generation_before_losses - generation_value) AS total_losses_kwh,
    COUNT(DISTINCT reading_date)        AS days_with_data
FROM generation_readings
GROUP BY tenant_id, plant_id, source_type_id, DATE_TRUNC('month', reading_date);


