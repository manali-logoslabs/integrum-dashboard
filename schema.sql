-- ──────────────────────────────────────────────────────────────
--  LAYER 2: TENANT (CUSTOMER) MANAGEMENT
-- ──────────────────────────────────────────────────────────────

-- One row per customer organisation. Root of all multi-tenancy.
CREATE TABLE tenants (
    tenant_id        SERIAL          PRIMARY KEY,
    tenant_code      VARCHAR(30)     NOT NULL UNIQUE,   -- short slug, e.g. 'GIL', 'C9'
    name             VARCHAR(200)    NOT NULL,
    org_type         VARCHAR(50),                       -- 'INDUSTRIAL', 'COMMERCIAL', 'CAPTIVE'
    state            VARCHAR(100),
    discom           VARCHAR(100),                      -- e.g. 'MSEDCL', 'BESCOM'
    gstin            VARCHAR(20),
    contact_email    VARCHAR(200),
    contact_phone    VARCHAR(20),
    is_active        BOOLEAN         NOT NULL DEFAULT TRUE,
    created_at       TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    updated_at       TIMESTAMPTZ     NOT NULL DEFAULT NOW()
);

-- Users scoped to a tenant
CREATE TABLE users (
    user_id          SERIAL          PRIMARY KEY,
    tenant_id        INT             NOT NULL REFERENCES tenants(tenant_id) ON DELETE CASCADE,
    username         VARCHAR(100)    NOT NULL,
    password_hash    VARCHAR(255)    NOT NULL,
    full_name        VARCHAR(200),
    email            VARCHAR(200),
    role             VARCHAR(30)     NOT NULL DEFAULT 'VIEWER',  -- 'ADMIN', 'ANALYST', 'VIEWER'
    is_active        BOOLEAN         NOT NULL DEFAULT TRUE,
    last_login_at    TIMESTAMPTZ,
    created_at       TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    UNIQUE (tenant_id, username),
    UNIQUE (tenant_id, email)
);


-- ──────────────────────────────────────────────────────────────
--  LAYER 3: PLANT & DEVICE HIERARCHY
-- ──────────────────────────────────────────────────────────────

-- Physical generation plants. A plant belongs to one tenant.
CREATE TABLE plants (
    plant_id             SERIAL          PRIMARY KEY,
    tenant_id            INT             NOT NULL REFERENCES tenants(tenant_id) ON DELETE CASCADE,
    plant_code           VARCHAR(50)     NOT NULL,
    plant_name           VARCHAR(200)    NOT NULL,
    location             VARCHAR(200),
    state                VARCHAR(100),
    latitude             DECIMAL(9,6),
    longitude            DECIMAL(9,6),
    total_capacity_mw    DECIMAL(10,3),
    commissioning_date   DATE,
    status               VARCHAR(30)     NOT NULL DEFAULT 'ACTIVE',  -- ACTIVE, INACTIVE, MAINTENANCE
    created_at           TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    updated_at           TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    UNIQUE (tenant_id, plant_code)
);

-- ┌─────────────────────────────────────────────────────────────┐
-- │  KEY DESIGN: Many-to-many between plants and energy sources │
-- │  Solar-only  → 1 row (SOLAR)                               │
-- │  Wind-only   → 1 row (WIND)                                │
-- │  Hybrid      → 2 rows (SOLAR + WIND)                       │
-- │  Future      → just INSERT a new energy_source_types row   │
-- └─────────────────────────────────────────────────────────────┘
CREATE TABLE plant_energy_sources (
    plant_source_id      SERIAL          PRIMARY KEY,
    plant_id             INT             NOT NULL REFERENCES plants(plant_id) ON DELETE CASCADE,
    source_type_id       INT             NOT NULL REFERENCES energy_source_types(source_type_id),
    installed_capacity_mw DECIMAL(10,3)  NOT NULL,
    commissioned_date    DATE,
    is_active            BOOLEAN         NOT NULL DEFAULT TRUE,
    created_at           TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    UNIQUE (plant_id, source_type_id)
);

-- Individual generation units: solar panels, wind turbines, inverters, etc.
CREATE TABLE devices (
    device_id            SERIAL          PRIMARY KEY,
    plant_id             INT             NOT NULL REFERENCES plants(plant_id) ON DELETE CASCADE,
    source_type_id       INT             NOT NULL REFERENCES energy_source_types(source_type_id),
    serial_number        VARCHAR(100)    NOT NULL,
    device_name          VARCHAR(200),
    device_model         VARCHAR(200),
    manufacturer         VARCHAR(200),
    capacity_kw          DECIMAL(10,3),
    status               VARCHAR(30)     NOT NULL DEFAULT 'ACTIVE',
    installed_at         DATE,
    decommissioned_at    DATE,
    created_at           TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    UNIQUE (plant_id, serial_number)
);


-- ──────────────────────────────────────────────────────────────
--  LAYER 4: GENERATION TIME-SERIES (15-MIN)
-- ──────────────────────────────────────────────────────────────

-- One row per device per 15-minute interval.
-- source_type_id makes solar and wind readings queryable in a
-- single table — no separate solar_generation / wind_generation tables.
CREATE TABLE generation_readings (
    reading_id               BIGSERIAL       PRIMARY KEY,
    tenant_id                INT             NOT NULL REFERENCES tenants(tenant_id),
    plant_id                 INT             NOT NULL REFERENCES plants(plant_id),
    device_id                INT             NOT NULL REFERENCES devices(device_id),
    source_type_id           INT             NOT NULL REFERENCES energy_source_types(source_type_id),
    reading_date             DATE            NOT NULL,
    reading_time             TIME            NOT NULL,   -- start of 15-min slot
    generation_value         DECIMAL(14,4)   NOT NULL DEFAULT 0,  -- net after losses (kWh)
    generation_before_losses DECIMAL(14,4)   NOT NULL DEFAULT 0,  -- gross (kWh)
    created_at               TIMESTAMPTZ     NOT NULL DEFAULT NOW()
) PARTITION BY RANGE (reading_date);

-- Monthly partitions for performance (extend as needed)
CREATE TABLE generation_readings_2025_04 PARTITION OF generation_readings
    FOR VALUES FROM ('2025-04-01') TO ('2025-05-01');
CREATE TABLE generation_readings_2025_05 PARTITION OF generation_readings
    FOR VALUES FROM ('2025-05-01') TO ('2025-06-01');
CREATE TABLE generation_readings_2025_06 PARTITION OF generation_readings
    FOR VALUES FROM ('2025-06-01') TO ('2025-07-01');
-- Add future partitions via: CREATE TABLE generation_readings_YYYY_MM PARTITION OF ...


