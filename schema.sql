-- ──────────────────────────────────────────────────────────────
--  LAYER 1: LOOKUP / REFERENCE TABLES
-- ──────────────────────────────────────────────────────────────

-- All supported energy source types.
-- Adding a new source (e.g. HYDRO) = one INSERT, zero DDL changes.
CREATE TABLE energy_source_types (
    source_type_id   SERIAL          PRIMARY KEY,
    code             VARCHAR(30)     NOT NULL UNIQUE,   -- 'SOLAR', 'WIND', 'HYDRO', ...
    name             VARCHAR(100)    NOT NULL,
    unit             VARCHAR(20)     NOT NULL DEFAULT 'kWh',
    description      TEXT,
    is_active        BOOLEAN         NOT NULL DEFAULT TRUE,
    created_at       TIMESTAMPTZ     NOT NULL DEFAULT NOW()
);

