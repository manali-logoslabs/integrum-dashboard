-- ──────────────────────────────────────────────────────────────
--  LAYER 6: SETTLEMENT & MATCHING (15-MIN)
-- ──────────────────────────────────────────────────────────────

-- How each device's generation is matched to consumption each slot.
CREATE TABLE settlement_matching (
    matching_id              BIGSERIAL       PRIMARY KEY,
    tenant_id                INT             NOT NULL REFERENCES tenants(tenant_id),
    plant_id                 INT             NOT NULL REFERENCES plants(plant_id),
    device_id                INT             NOT NULL REFERENCES devices(device_id),
    source_type_id           INT             NOT NULL REFERENCES energy_source_types(source_type_id),
    settlement_date          DATE            NOT NULL,
    settlement_time          TIME            NOT NULL,
    tod_slot_id              INT             REFERENCES tod_slots(slot_id),
    generation_value         DECIMAL(14,4)   NOT NULL DEFAULT 0,
    generation_before_losses DECIMAL(14,4)   NOT NULL DEFAULT 0,
    slot_total_consumption   DECIMAL(14,4)   NOT NULL DEFAULT 0,
    allocated_consumption    DECIMAL(14,4)   NOT NULL DEFAULT 0,
    surplus_generation       DECIMAL(14,4)   NOT NULL DEFAULT 0,
    surplus_gen_with_banking DECIMAL(14,4)   NOT NULL DEFAULT 0,
    matched_settlement       DECIMAL(14,4)   NOT NULL DEFAULT 0,
    created_at               TIMESTAMPTZ     NOT NULL DEFAULT NOW()
) PARTITION BY RANGE (settlement_date);

CREATE TABLE settlement_matching_2025_04 PARTITION OF settlement_matching
    FOR VALUES FROM ('2025-04-01') TO ('2025-05-01');
CREATE TABLE settlement_matching_2025_05 PARTITION OF settlement_matching
    FOR VALUES FROM ('2025-05-01') TO ('2025-06-01');

-- 15-min slot aggregate (all devices combined)
CREATE TABLE slot_summary (
    slot_summary_id          BIGSERIAL       PRIMARY KEY,
    tenant_id                INT             NOT NULL REFERENCES tenants(tenant_id),
    plant_id                 INT             NOT NULL REFERENCES plants(plant_id),
    summary_date             DATE            NOT NULL,
    summary_time             TIME            NOT NULL,
    tod_slot_id              INT             REFERENCES tod_slots(slot_id),
    generation_value         DECIMAL(14,4)   NOT NULL DEFAULT 0,  -- combined all sources
    slot_total_consumption   DECIMAL(14,4)   NOT NULL DEFAULT 0,
    allocated_consumption    DECIMAL(14,4)   NOT NULL DEFAULT 0,
    surplus_generation       DECIMAL(14,4)   NOT NULL DEFAULT 0,
    surplus_gen_with_banking DECIMAL(14,4)   NOT NULL DEFAULT 0,
    matched_settlement       DECIMAL(14,4)   NOT NULL DEFAULT 0,
    created_at               TIMESTAMPTZ     NOT NULL DEFAULT NOW()
);


